const month = "june"
switch (month) {
    case "Jan":
        console.log("Jan");
        
        break;
    case "Feb":
        console.log("Feb");
        
        break;
    case "march":
        console.log("March");
        
        break;
    case "april":
        console.log("April");
        
        break;
    case "may":
        console.log("May");
        
        break;
    case "june":
        console.log("June");
        
        break;

    default:
        console.log("default case match");
        
        break;
}